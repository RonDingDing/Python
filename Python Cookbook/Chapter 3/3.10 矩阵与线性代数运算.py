print(m)
# [[ 1 -2  3]
#  [ 0  4  5]
#  [ 7  8 -9]]
print(m.T)
# [[ 1  0  7]
#  [-2  4  8]
#  [ 3  5 -9]]
print(m.I)
# [[ 0.33043478 -0.02608696  0.09565217]
#  [-0.15217391  0.13043478  0.02173913]
#  [ 0.12173913  0.09565217 -0.0173913 ]]

v = np.matrix([[2], [3], [4]])
print(v)
# [[2]
#  [3]
#  [4]]

